/**
 * Adds two numbers.
 *
 * @example
 * ```ts:[test] 正の数の足し算
 * assert.strictEqual(add(2, 3), 5);
 * ```
 *
 * @example
 * ```ts:[test]
 * assert.strictEqual(add(10, -5), 5);
 * assert.strictEqual(add(10, -3), 7);
 * ```
 */
export function add(a: number, b: number): number {
  return a + b;
}
  